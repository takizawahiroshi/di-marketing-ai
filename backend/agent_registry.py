def build_agent_list_text() -> str:
    """Planner のシステムプロンプト用エージェント一覧テキスト"""
    lines = []
    for aid, meta in AGENT_REGISTRY.items():
        lines.append(f"- {aid} ({meta['cat']}): {meta['name']} — {meta['desc'][:40]}")
    return "\n".join(lines)


def build_agent_sys_prompt(
    agent_id: str,
    context: dict,
    chained_context: str = "",
) -> str:
    """
    エージェントのシステムプロンプトを生成する。
    DI_KNOWLEDGE を knowledge_loader から取得して注入する。
    """
    knowledge = get_knowledge_text(max_chars=6000)
    title = _AGENT_TITLES.get(agent_id, agent_id)
    rules = _AGENT_RULES.get(agent_id, "日本語で丁寧に回答してください。")

    parts = [
        f"あなたは株式会社デジタルアイデンティティ（DI）の{title}です。"
        "以下の知識をベースに回答してください。",
        knowledge,
        f"【あなたの役割と動作ルール】\n{rules}",
    ]

    # コンテキスト注入
    ctx_parts = []
    if context.get("survey"):
        ctx_parts.append(f"【調査データ】\n{context['survey'][:1000]}")
    if context.get("persona"):
        ctx_parts.append(f"【ペルソナ】\n{context['persona'][:500]}")
    if context.get("brief"):
        ctx_parts.append(f"【ブリーフ】\n{context['brief'][:500]}")
    if chained_context:
        ctx_parts.append(f"【前エージェントからのバトン（引き継ぎ情報）】\n{chained_context}")

    if ctx_parts:
        parts.append("\n\n".join(ctx_parts))

    return "\n\n".join(parts)


